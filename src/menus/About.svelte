<h2>Data Sources</h2>
<p>
  FEVer utilizes an enormous amount of scientifically complex data, allowing you
  to explore the interconnectedness of weather and climate, including between
  polar regions and the rest of the planet.
</p>
<p>Data sets include:</p>
<dl>
  <dt>Weather</dt>
  <dd>GFS (Global Forecast System)</dd>

  <dt>Ocean Currents</dt>
  <dd>OSCAR (Ocean Surface Current Analysis Real-time)</dd>

  <dt>Sea Surface Temperature</dt>
  <dd>RTGSST (Real Time Global Sea Surface Temperature)</dd>

  <dt>Aerosols and Chemistry</dt>
  <dd>GEOS-5 (Goddard Earth Observing System, Version 5)</dd>
</dl>

